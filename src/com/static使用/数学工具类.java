package com.static使用;

/**
 * 定义数学工具类
 * 工具使用静态的方法
 */
public class 数学工具类 {
    // 定义了工具类 此时使用静态的方法
    public static int sumInt(int num1,int num2){
        return num1 + num2;
    }

    public static int divideInt(int num1,int num2){
        return num1 / num2;
    }

    public static int mulInt(int num1,int num2){
        return num1 * num2;
    }
}
