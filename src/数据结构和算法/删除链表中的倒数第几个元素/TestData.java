package 数据结构和算法.删除链表中的倒数第几个元素;

public class TestData {
    public static void main(String[] args) {
        int n = 4;
        while (n!=0){
            System.out.println(n);
            n--;
        }

        System.out.println("==================");
        int n2 = 4;
        while (n2-- > 0){
            System.out.println(n2);
        }
    }
}
